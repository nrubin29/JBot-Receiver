package me.nrubin29.jbot.receiver;

/**
 * Represents the direction of a d-pad.
 * @author Noah Rubin
 */
public enum ControllerDirection {

	/**
	 * The up direction.
	 */
	UP,
	
	/**
	 * The down direction.
	 */
	DOWN,
	
	/**
	 * The left direction.
	 */
	LEFT,
	
	/**
	 * The right direction.
	 */
	RIGHT,
	
	/**
	 * The center direction (not moved).
	 */
	CENTER
}